        #region Private Methods
        public static Dictionary<string, int> _dicIndex = new Dictionary<string, int>();
	    public static void FillDicIndex(IDataReader dataReader)
	    {
	        for (int i = 0; i < dataReader.FieldCount; i++)
	        {
	            var columnName = dataReader.GetName(i);
	            _dicIndex[columnName.ToLower()] = i;
	        }
	    }
	    public static int GetIndex(string name)
	    {
	        if (_dicIndex.ContainsKey(name.ToLower()))
	            return _dicIndex[name.ToLower()];
	        //LogTo.Info("Khong tim thay column name = " + name.ToLower());
	        return -1;
	    }

    internal void PopulateBusinessObjectFromReader(Role businessObject, IDataReader dataReader)
        {
            if (_dicIndex.Count == 0) FillDicIndex(dataReader);
		
			if (GetIndex(Role.RoleFields.CreatedAt.ToString()) != -1)
				if (!dataReader.IsDBNull(GetIndex(Role.RoleFields.CreatedAt.ToString())))
				{
					businessObject.CreatedAt = dataReader.GetDateTime(GetIndex(Role.RoleFields.CreatedAt.ToString()));
				}

			if (GetIndex(Role.RoleFields.CreatedBy.ToString()) != -1)
				businessObject.CreatedBy = dataReader.GetInt32(GetIndex(Role.RoleFields.CreatedBy.ToString()));

			if (GetIndex(Role.RoleFields.Description.ToString()) != -1)
				businessObject.Description = dataReader.GetString(GetIndex(Role.RoleFields.Description.ToString()));

				businessObject.Id = dataReader.GetInt32(GetIndex(Role.RoleFields.Id.ToString()));

			if (GetIndex(Role.RoleFields.Name.ToString()) != -1)
				businessObject.Name = dataReader.GetString(GetIndex(Role.RoleFields.Name.ToString()));

			if (GetIndex(Role.RoleFields.UpdatedAt.ToString()) != -1)
				if (!dataReader.IsDBNull(GetIndex(Role.RoleFields.UpdatedAt.ToString())))
				{
					businessObject.UpdatedAt = dataReader.GetDateTime(GetIndex(Role.RoleFields.UpdatedAt.ToString()));
				}

			if (GetIndex(Role.RoleFields.UpdatedBy.ToString()) != -1)
				businessObject.UpdatedBy = dataReader.GetInt32(GetIndex(Role.RoleFields.UpdatedBy.ToString()));


        }

